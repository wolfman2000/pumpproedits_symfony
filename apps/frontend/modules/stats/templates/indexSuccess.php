    Make sure your edit follows it.
</p>
<pre>
#SONG:<var>Song Name</var>;
#NOTES:
<var>pump-single OR pump-double</var>:
<var>EditNameHere</var>:
Edit:
<var>1-99</var>:
<var>Comma separated list of 5, 11, or 22 numbers on a single line</var>:

00100
00000
00000
00000
,
10101
00000
01110
00000
;
</pre>

